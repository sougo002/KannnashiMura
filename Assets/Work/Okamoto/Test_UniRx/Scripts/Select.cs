using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UniRx;
using UniRx.Triggers;

public class Select : Cube {

	//********************************************************************************
	//MACRO
	//********************************************************************************
	//CLASS BEFORE
	//********************************************************************************
	//STRUCTURE
	//********************************************************************************
	//CLASS AFTER
	//********************************************************************************
	//PUBLIC
	//********************************************************************************
	//PRIVATE
	//********************************************************************************

	//******************************
	//Start
	//起動後初回に実行される
	//******************************
	void Start ()
	{
		//初期座標
		Observable.Return(new Vector3(0.0f, 20.0f, 0.0f))
			.Subscribe(v => gameObject.transform.position = v);

		//Selectを使って入力を加工してあげる
		this.UpdateAsObservable()
			.Select(_ => 2)
			.Subscribe(l => Move(0.01f * l, 0));
	}
}
