using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UniRx;
using UniRx.Triggers;

public class First : Cube {

	//********************************************************************************
	//MACRO
	//********************************************************************************
	//CLASS BEFORE
	//********************************************************************************
	//STRUCTURE
	//********************************************************************************
	//CLASS AFTER
	//********************************************************************************
	//PUBLIC
	//********************************************************************************
	//PRIVATE
	//********************************************************************************

	//******************************
	//Start
	//起動後初回に実行される
	//******************************
	void Start ()
	{
		//初期位置
		Observable.Return(new Vector3(0.0f, -10.0f, 0.0f))
			.Subscribe(pos => this.gameObject.transform.localPosition = pos);

		//左クリックされたら移動するお
		this.UpdateAsObservable()
			.First(input => Input.GetMouseButton(0))	//初回のみ
			.Subscribe(pos => Move(10.0f, 0.0f));
	}
}
