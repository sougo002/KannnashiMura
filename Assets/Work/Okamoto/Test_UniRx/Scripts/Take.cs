using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UniRx;
using UniRx.Triggers;

public class Take : Cube {

	//********************************************************************************
	//MACRO
	//********************************************************************************
	//CLASS BEFORE
	//********************************************************************************
	//STRUCTURE
	//********************************************************************************
	//CLASS AFTER
	//********************************************************************************
	//PUBLIC
	//********************************************************************************
	//PRIVATE
	//********************************************************************************

	//******************************
	//Start
	//起動後初回に実行される
	//******************************
	void Start ()
	{
		//初期座標
		Observable.Return(new Vector3(0.0f, -20.0f, 0.0f))
			.Subscribe(pos => this.gameObject.transform.localPosition = pos);

		//100frame中は移動する
		this.UpdateAsObservable()
			.Take(120)  //フレーム指定
			.Subscribe(obj => Move(1.0f, 0.0f));
	}
}
